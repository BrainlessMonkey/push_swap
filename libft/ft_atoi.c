#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	s;
	int	r;
	int	i;

	s = 1;
	r = 0;
	i = 0;
	while (nptr[i] == 32 || (nptr[i] > 8 && nptr[i] < 14))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
		if (nptr[i++] == '-')
			s = -s;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		r = (r * 10) + (s * (nptr[i] - 48));
		i++;
	}
	return (r);
}
